fn render_file_replay(spec: &RerunDashboardSpec) -> RenderedLaunch {
    let files = spec
        .data_source
        .files
        .as_ref()
        .expect("FileReplay DataSource must populate `files`");
    let mount = files
        .mount_path
        .as_deref()
        .unwrap_or(DEFAULT_REPLAY_MOUNT);
    let globs: Vec<&str> = if files.globs.is_empty() {
        vec!["*.rrd"]
    } else {
        files.globs.iter().map(String::as_str).collect()
    };

    let live = effective_live_config(spec.data_source.live.as_ref());
    let live_port = live
        .port
        .unwrap_or_else(|| resolve_live_port(&spec.rerun_version, live.transport));
    let web_port = effective_web_port(spec);
    let version = &spec.rerun_version;

    // CLI: `rerun --serve-web --bind :: --port <grpc> --web-viewer-port <web>
    //         <expanded paths>`. Expansion happens via `sh -c` glob expansion;
    // we cd into the mount so user-supplied globs are relative.
    let mut tokens: Vec<String> = vec![
        "rerun".to_string(),
        "--serve-web".to_string(),
        "--bind".to_string(),
        "::".to_string(),
        "--port".to_string(),
        live_port.to_string(),
    ];
    if let Some(wp) = web_port {
        tokens.push("--web-viewer-port".to_string());
        tokens.push(wp.to_string());
    }
    if let Some(limit) = &live.memory_limit {
        tokens.push("--server-memory-limit".to_string());
        tokens.push(limit.clone());
    }
    // Glob args appended last, kept unquoted in the rendered shell so the
    // shell expands them at runtime.
    let cmd_prefix = tokens.join(" ");
    let glob_args = globs.join(" ");
    let shell = format!(
        "pip install --no-cache-dir --quiet 'rerun-sdk=={version}' && cd {mount} && \
         exec {cmd_prefix} {glob_args}"
    );

    RenderedLaunch {
        python: None,
        shell,
        live_port: Some(live_port),
        web_port,
        transport: Some(live.transport),
    }
}

// -----------------------------------------------------------------------------
// helpers
// -----------------------------------------------------------------------------

fn py_str(s: &str) -> String {
    let escaped = s.replace('\\', "\\\\").replace('"', "\\\"");
    format!("\"{escaped}\"")
}

fn py_bool(b: bool) -> &'static str {
    if b { "True" } else { "False" }
}

fn py_opt_str(s: Option<&str>) -> String {
    match s {
        Some(s) => py_str(s),
        None => "None".to_string(),
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use rerun_operator_api::v1alpha1::{
        Blueprint, Container, DataSource, DataSourceKind, FileReplayConfig, IngressConfig,
        LeafView, LiveStreamConfig, Presentation, RerunDashboardSpec, View, Visibility,
        WireTransport,
    };

    fn live_stream_spec() -> RerunDashboardSpec {
        RerunDashboardSpec {
            rerun_version: "0.31.3".into(),
            application_id: Some("spot".into()),
            blueprint: Blueprint {
                collapse_panels: true,
                root: View::Horizontal(Container {
                    children: vec![
                        View::Spatial3D(LeafView {
                            origin: "/".into(),
                            contents: vec!["+/**".into()],
                            name: Some("Robot".into()),
                        }),
                        View::TimeSeries(LeafView {
                            origin: "/metrics".into(),
                            contents: vec![],
                            name: None,
                        }),
                    ],
                    shares: vec![3, 1],
                    name: None,
                }),
            },
            data_source: DataSource {
                kind: DataSourceKind::LiveStream,
                live: Some(LiveStreamConfig {
                    transport: WireTransport::Grpc,
                    port: None,
                    memory_limit: None,
                }),
                files: None,
            },
            presentation: Presentation {
                web: true,
                web_port: None,
                ingress: IngressConfig {
                    visibility: Visibility::Cluster,
                    ..Default::default()
                },
            },
            resources: None,
        }
    }

    #[test]
    fn renders_horizontal_with_shares() {
        let r = render("spot", &live_stream_spec());
        let py = r.python.expect("python source for LiveStream");
        assert!(py.contains("rrb.Blueprint("));
        assert!(py.contains("rrb.Horizontal("));
        assert!(py.contains("rrb.Spatial3DView(origin=\"/\""));
        assert!(py.contains("rrb.TimeSeriesView(origin=\"/metrics\""));
        assert!(py.contains("column_shares=[3, 1]"));
        assert!(py.contains("collapse_panels=True"));
    }

    #[test]
    fn renders_run_fn_with_ports() {
        let r = render("spot", &live_stream_spec());
        let py = r.python.expect("python source for LiveStream");
        assert!(py.contains("def run():"));
        assert!(py.contains("rr.init(\"spot\""));
        // 0.31.3 + Grpc → gRPC port 9876 / web port 9090.
        assert!(py.contains("rr.serve_grpc(grpc_port=9876"));
        assert!(py.contains("rr.serve_web_viewer(web_port=9090"));
        assert!(py.contains("make_active=True"));
        assert_eq!(r.live_port, Some(9876));
        assert_eq!(r.web_port, Some(9090));
        assert_eq!(r.transport, Some(WireTransport::Grpc));
    }

    #[test]
    fn renders_legacy_websocket() {
        let mut spec = live_stream_spec();
        spec.rerun_version = "0.22.1".into();
        spec.data_source.live = Some(LiveStreamConfig {
            transport: WireTransport::WebSocket,
            port: None,
            memory_limit: Some("25%".into()),
        });
        let r = render("spot", &spec);
        let py = r.python.expect("python source");
        assert!(py.contains("rr.serve_web(open_browser=False"));
        assert!(py.contains("ws_port=9877"));
        assert!(py.contains("web_port=9090"));
        assert!(py.contains("server_memory_limit=\"25%\""));
        assert_eq!(r.live_port, Some(9877));
        assert_eq!(r.transport, Some(WireTransport::WebSocket));
    }

    #[test]
    fn emits_leaf_contents_filter() {
        let r = render("spot", &live_stream_spec());
        let py = r.python.expect("python source");
        assert!(py.contains("contents=[\"+/**\"]"));
    }

    #[test]
    fn escapes_quotes_in_strings() {
        let mut spec = live_stream_spec();
        spec.application_id = Some("spot\"evil".into());
        let r = render("spot\"evil", &spec);
        let py = r.python.expect("python source");
        assert!(py.contains("rr.init(\"spot\\\"evil\""));
    }

    #[test]
    fn live_stream_shell_installs_sdk_and_runs_python() {
        let r = render("spot", &live_stream_spec());
        assert!(r.shell.contains("pip install"));
        assert!(r.shell.contains("rerun-sdk==0.31.3"));
        assert!(r.shell.contains("python -u /opt/rerun/blueprint/blueprint.py"));
    }

    #[test]
    fn file_replay_emits_cli_invocation() {
        let mut spec = live_stream_spec();
        spec.data_source = DataSource {
            kind: DataSourceKind::FileReplay,
            live: None,
            files: Some(FileReplayConfig {
                pvc: "training-recordings".into(),
                globs: vec![],
                mount_path: None,
                file_retention_seconds: None,
            }),
        };
        let r = render("spot", &spec);
        assert!(r.python.is_none());
        assert!(r.shell.contains("pip install"));
        assert!(r.shell.contains("rerun-sdk==0.31.3"));
        assert!(r.shell.contains("cd /var/lib/rerun/recordings"));
        assert!(r.shell.contains("rerun --serve-web --bind :: --port 9876"));
        assert!(r.shell.contains("--web-viewer-port 9090"));
        assert!(r.shell.contains("*.rrd"));
        assert_eq!(r.live_port, Some(9876));
    }

    #[test]
    fn file_replay_honors_custom_globs_and_mount() {
        let mut spec = live_stream_spec();
        spec.data_source = DataSource {
            kind: DataSourceKind::FileReplay,
            live: None,
            files: Some(FileReplayConfig {
                pvc: "rrds".into(),
                globs: vec!["session-*.rrd".into(), "blueprint.rbl".into()],
                mount_path: Some("/data/rrds".into()),
                file_retention_seconds: None,
            }),
        };
        let r = render("spot", &spec);
        assert!(r.shell.contains("cd /data/rrds"));
        assert!(r.shell.contains("session-*.rrd blueprint.rbl"));
    }

    #[test]
    fn presentation_disables_web_when_false() {
        let mut spec = live_stream_spec();
        spec.presentation.web = false;
        let r = render("spot", &spec);
        let py = r.python.expect("python source");
        assert!(!py.contains("rr.serve_web_viewer"));
        assert_eq!(r.web_port, None);
    }
}
